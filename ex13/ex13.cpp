#include "Rank.tpp"

int	main( void )
{
	Matrix u1({
		{1., 0., 0.},
		{0., 1., 0.},
		{0., 0., 1.}
	});
	std::cout << u1.rank() << std::endl; // 3

	Matrix u2({
		{1., 2., 0., 0.},
		{2., 4., 0., 0.},
		{-1., 2., 1., 1.}
	});
	std::cout << u2.rank() << std::endl; // 2

	Matrix u3({
		{8., 5., -2.},
		{4., 7., 20.},
		{7., 6., 1.},
		{21., 18., 7.}
	});
	std::cout << u3.rank() << std::endl; // 3

	Matrix<Complex> c1({
		{ {1, 2}, {2, 0} },
		{ {2, 0}, {4, -1} }
	});
	std::cout << c1.rank() << std::endl; // 2

	return (0);
}